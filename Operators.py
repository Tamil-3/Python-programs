# Arithmetic Operators -> Exponent , Floor Division
a = 2
b = 3
print(a ** b)
print(a // b)

x = 4
x += 1    #x = x + 1
x -= 3   # x = x - 3
x *= 2   # x = x * 2

# Comparison Operator -> returns boolean value
print(5 != 5)

# Logical Operator
print(5 > 5 and 3 > 2)
print(5 < 3 or 3 > 1)
print(not (5 > 3))

# Bitwise Operator -> &, |
print(5 | 6)

# BODMAS
print(10 / 3 * 5 + 2 - 1)